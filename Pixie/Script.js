//window means pooore project pe 
window.addEventListener("wheel",function(dets){
    if(dets.deltaY>0){
        gsap.to(".marq",{
            transform:'translateX(-200%)',
            repeat:-1,
            duration:6,
            ease:'none',
        })
        gsap.to(".marq i",{
            rotate:180,
        });
    }
    else{
        gsap.to(".marq",{
            transform:'translateX(0%)',
            repeat:-1,
            duration:6,
            ease:'none',
        })
        gsap.to(".marq i",{
            rotate:0,
        });
    }})
